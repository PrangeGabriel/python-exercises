n1=int(input('Digite um numero inteiro '))
print('\no numero digitado é {}. \n Seu antecessor é {} e seu predescessor é {}.'.format(n1, n1-1, n1+1))
print('o dobro de {} é {}, elevado ao quadrado é {} e sua raiz quadrada é {}'. format(n1, n1*2, n1**2, n1**(1/2)))

print('\n\no numero digitado em metros é %2.2f. \n Em centimetros é %.2fcm e em milimetros é %.2fmm.'%(n1, n1*10, n1*100))
